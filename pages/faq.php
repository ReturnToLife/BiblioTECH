<h3 id="faq1">Qui peut emprunter ?</h3>

Tout le monde. Il vous faut juste une adresse mail @epitech.eu.

<h3 id="faq2">Qui peut pr&ecirc;ter ?</h3>

Tous ceux qui veulent. Enregistrez vos livres si vous &ecirc;tes partageurs !

<h3 id="faq3">Est-ce que BiblioTECH garantit les pr&ecirc;ts ? Qui paye si le livre est abim&eacute; ?</h3>

BiblioTECH vous met en relation avec le propri&eacute;taire du livre. A vous et &agrave; lui de voir comment vous vous arrangez. Le LSE et l'ACSEL, par exemple, vous invitent &agrave; consulter les livres sur place mais pas &agrave; les emprunter. Chacun fait comme il pr&eacute;f&egrave;re.

<h3 id="faq4">Je veux lire un bouquin int&eacute;ressant, mais je ne sais pas lequel !</h3>

BiblioTECH permet aussi de noter les livres, de les commenter, et de vous recommander de saines et int&eacute;ressantes lectures.

<h3 id="faq5">Je veux enregistrer mon dernier num&eacute;ro de PlayGirl !</h3>

Comme son nom l'indique, le but est de partager des livres relatifs &agrave; l'&eacute;cole ou &agrave; l'informatique en g&eacute;n&eacute;ral. Le Guide du Voyageur Galactique est OK, La Grande Aventure de Babar ne l'est pas.

<h3 id="faq6">Qui g&egrave;re &ccedil;a ?</h3>

La ravissante et inarr&ecirc;table Barbara Lepage a mis en place le projet. Le Koalab fournit les serveurs.

